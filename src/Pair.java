public class Pair {
    private String key;
    private String value;
    // Stores key-value pair in a HashMap
    public Pair(String key, String value) {
        this.key = key;
        this.value = value;
    }
    // Setters and getters
    public String getKey() {
        return key;
    }
    public String getValue() {
        return value;
    }
}
